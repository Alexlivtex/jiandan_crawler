def hasNextPage(soup):
    for tag in soup.find_all('a'):
        if tag.has_attr('class') and tag.attrs['class'][0] == "next-comment-page":
            return True
    return False

def writeTag():
    if os.path.exists(tag_file):
        os.remove(tag_file)
    f = open(tag_file, 'wb')
    f.write(str(current_page))
    f.close()

def readTag():
    global current_page
    if not os.path.exists(tag_file):
        writeTag()
    else:
        file_obj = open(tag_file)
        text = file_obj.read()
        print(text)
        current_page = int(text) + 1


def fetchWeb(url):
    header_item = user_agent_list[random.randint(0, len(user_agent_list) - 1)]
    headers = {'User-Agent': header_item,
       'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
       'Accept-Charset': 'ISO-8859-1,utf-8;q=0.7,*;q=0.3',
       'Accept-Encoding': 'none',
       'Accept-Language': 'en-US,en;q=0.8',
       'Connection': 'keep-alive'}
    req = urllib2.Request(url, headers=headers)
    response = urllib2.urlopen(req)
    html = response.read()
    return html

def download_pictures(url):
    if not os.path.exists(Pict_Path + str(current_page)):
        os.makedirs(Pict_Path + str(current_page))

    file_name = Pict_Path + str(current_page) + "\\" + url.split("/")[-1]
    if os.path.exists(file_name):
        print("File already exists")
        return

    hdr = {'User-Agent': 'Mozilla/5.0 (X11; Linux x86_64) AppleWebKit/537.11 (KHTML, like Gecko) Chrome/23.0.1271.64 Safari/537.11',
       'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
       'Accept-Charset': 'ISO-8859-1,utf-8;q=0.7,*;q=0.3',
       'Accept-Encoding': 'none',
       'Accept-Language': 'en-US,en;q=0.8',
       'Connection': 'keep-alive'}


    try:
        req = urllib2.Request(url, headers=hdr)
        connect = urllib2.urlopen(req, timeout=20)
        f = open(file_name, 'wb')
        f.write(connect.read())
        f.close()
    except:
        f = open(file_name.split('.')[0], 'wb')
        f.write(url)
        f.close()


